// =======================
// What is Sliding Window?
// =======================
//
// Sliding Window is a technique used to process a range (window) of elements
// in an array or string efficiently by moving the window instead of
// recalculating everything again and again.
//
// Instead of:
// - Recomputing results for every subarray (inefficient)
//
// We:
// - Reuse previous computation (efficient)
//
//
// When do we use Sliding Window?
// ==============================
//
// Use this technique when the problem involves:
// - Subarray
// - Substring
// - Window of size k
// - Maximum / Minimum sum
// - Longest / Smallest length
//
// Especially useful when working with
// continuous elements in arrays or strings.
//
